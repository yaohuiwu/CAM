public class User3 {

    private String id;
    private int age;
    private String name;
    private boolean isEducated;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isEducated() {
        return isEducated;
    }

    public void setEducated(boolean isEducated) {
        this.isEducated = isEducated;
    }
}
